package JavaPractice;

public class BreakStmt {
    public static void main(String[] args) {
        int n = 1;

        switch(n){
            case 1:
                System.out.println("Prachi");
                break;
            case 2:
                System.out.println("Manthan");
                break;
            default:
                System.out.println("default case");
        }
    }
}
